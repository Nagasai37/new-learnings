#find the vowels in the string 
b = "anaslokesh"
for j in b:
    count = 0
    if j == "a":
        count = count+1
       
        count +=1
        
    elif j == "e":
       c = count+1
       c +=1
    elif j == "i":
        d = c+1
        d +=1
    elif j == "o":
        e = d+1    
        e +=1
    elif j == "u":
        f = e+1
        f +=1
    else:
        print("no vowels")    
    print(count)        
